package ex_10_ForLoop;

public class Lab_109_Greater_vs_Equal {
    public static void main(String[] args) {
        for(int i = 0; i <= 10; i++){
            System.out.println(i);
        }

        for(int j = 1; j <= 10; j++){
            System.out.println(j);
        }

        for(int j = 1; j <= 10; ++j){
            System.out.println(j);
        }
    }
    }

