package ex_10_ForLoop;

public class Lab_103_For_If_Loop {
    public static void main(String[] args) {
        for(int pratik = 0; pratik <18; pratik++){
            if(pratik >15){
                System.out.println("He can get iphone");
            }else{
                System.out.println("No iphone");
            }
        }
    }
}
