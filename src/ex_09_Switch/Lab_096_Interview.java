package ex_09_Switch;

public class Lab_096_Interview {
    public static void main(String[] args) {
        char code = 'C';

        switch(code){
            default:
                System.out.println("Helloooo");
            case 'A':
                System.out.println("Case A");
            case 'B':
                System.out.println("Case B");
        }
    }
}
