package ex_09_Switch;

public class Lab_095_Interview {
    public static void main(String[] args) {
        int a = 11;
        switch(-1){
            default:
                System.out.println("Default");
                break;
            case -1:
                System.out.println("-1");
                break;
            case 1:
                System.out.println("1");
                break;
        }
    }
}
