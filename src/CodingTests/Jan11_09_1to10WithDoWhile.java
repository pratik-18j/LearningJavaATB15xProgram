package CodingTests;

//Write a Java program to print numbers from 1 to 10 using a do-while loop.
public class Jan11_09_1to10WithDoWhile {
    public static void main(String[] args) {
        int i =1;
        do{
            System.out.println(i);
            i++;
        }
        while(i<=10);

    }
}
