package ex_04_Operators;

public class Lab_049_All_Operators {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println(a + b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);

        //Compound Assignment operator

        int age = 10;
        //age += 10;
        age -= 10;
        System.out.println(age);



    }
}
