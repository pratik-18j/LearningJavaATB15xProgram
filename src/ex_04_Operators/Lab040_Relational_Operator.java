package ex_04_Operators;

public class Lab040_Relational_Operator {
    public static void main(String[] args) {

        //Always returns the value in boolean format

        int a = 10;
        int b = 20;

        boolean c = a > b;
        System.out.println(c);

        boolean d = a < b;
        System.out.println(d);
    }
}
