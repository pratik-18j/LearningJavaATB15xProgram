package ex_04_Operators;

public class Lab037_Arithmatic_Operators {
    public static void main(String[] args) {
        //Arithmatic operators
        //Addition
        //substraction
        //Multiplication
        //division
        //% Modulus

        int a = 10;
        int b = 4;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        /**
         * all mentioned above is Binary operator since it requires two operands
         */
        float c= 3.15f;
        System.out.println(a/c);

    }
}
