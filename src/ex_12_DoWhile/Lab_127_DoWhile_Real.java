package ex_12_DoWhile;

public class Lab_127_DoWhile_Real {
    public static void main(String[] args) {
        int number = 0;
        //It can be 0 - 10 elements or students
        do{
            System.out.println(number);
            //call function number
            //close the file, open the file
            number++;
        }while(number < 0);
    }
}
